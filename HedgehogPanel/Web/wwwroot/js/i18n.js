/* Simple global state w/ subscriber list. */
window.__hh_lang = window.__hh_lang || "cs";
window.__hh_lang_listeners = window.__hh_lang_listeners || new Set();
function useLang() {
  const [lang, setLangState] = React.useState(window.__hh_lang);
  React.useEffect(() => {
    const onChange = l => setLangState(l);
    window.__hh_lang_listeners.add(onChange);
    return () => window.__hh_lang_listeners.delete(onChange);
  }, []);
  const setLang = React.useCallback(l => {
    window.__hh_lang = l;
    window.__hh_lang_listeners.forEach(fn => fn(l));
  }, []);
  return [lang, setLang];
}

/* useT() — returns translator bound to current lang. */
function useT() {
  const [lang] = useLang();
  return React.useCallback((key, fallback) => TRANSLATIONS[lang]?.[key] ?? TRANSLATIONS.cs[key] ?? fallback ?? key, [lang]);
}
Object.assign(window, {
  TRANSLATIONS,
  useLang,
  useT
});